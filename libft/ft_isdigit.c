#include "libft.h"

int	ft_isdigit(char *str)
{
	while (*str)
	{
		if (str[0] == '-' || str[0] == '+')
			str++;
		if (*str < 48 || *str > 57)
			return (0);
		else
			str++;
	}
	return (1);
}
